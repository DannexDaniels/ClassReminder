package com.dannextech.apps.classreminder;

public class ClassModel {
    String cCode, cVenue, cName, cDate, cTime, cReminder;

    public ClassModel() {
    }

    public String getcCode() {
        return cCode;
    }

    public void setcCode(String cCode) {
        this.cCode = cCode;
    }

    public String getcVenue() {
        return cVenue;
    }

    public void setcVenue(String cVenue) {
        this.cVenue = cVenue;
    }

    public String getcName() {
        return cName;
    }

    public void setcName(String cName) {
        this.cName = cName;
    }

    public String getcDate() {
        return cDate;
    }

    public void setcDate(String cDate) {
        this.cDate = cDate;
    }

    public String getcTime() {
        return cTime;
    }

    public void setcTime(String cTime) {
        this.cTime = cTime;
    }

    public String getcReminder() {
        return cReminder;
    }

    public void setcReminder(String cReminder) {
        this.cReminder = cReminder;
    }
}
